            <p class="card-text"><p>Machine learning is a subset of artificial intelligence that deals with the development of algorithms and statistical models which enables computer systems to automatically improve their performance on a specific task by learning from data, without being explicitly programmed.</p>
<h3>Why Machine Learning is important?</h3>
<ul>
  <li>It is widely used in various fields like healthcare, finance, business, marketing, etc. to make informed decisions based on data.</li>
  <li>Machine learning can automate tasks that are time-consuming and require human effort.</li>
  <li>It can help in detecting fraud and identifying patterns that are not easily visible to humans.</li>
  <li>It can improve the accuracy and efficiency of predictions and decision-making.</li>
</ul>
<h3>Types of Machine Learning</h3>
<p>There are three main types of machine learning:</p>
<ul>
  <li><strong>Supervised Learning:</strong> In this type of learning, the algorithm is trained on labeled data, where the input and output are known. The aim is to make predictions on new, unseen data.</li>
  <li><strong>Unsupervised Learning:</strong> In this type of learning, the algorithm is trained on unlabeled data, where the input is known but the output is unknown. The aim is to find hidden patterns and structures in the data.</li>
  <li><strong>Reinforcement Learning:</strong> In this type of learning, the algorithm learns by interacting with an environment. It receives feedback in the form of rewards or penalties for its actions and adjusts its behavior to maximize the rewards.</li>
</ul>
